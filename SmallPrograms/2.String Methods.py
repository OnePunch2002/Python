name = "doraemon"

# Length of string
print(len(name))

# Character's position in string
print(name.find("r"))

# Capitalize the first letter in string
print(name.capitalize())

# UpperCase
print(name.upper())

# LowerCase
print(name.lower())

# Is it a digit?
print(name.isdigit())

# Is it a alphabet?
print(name.isalpha())

# Count the no. of characters in string
print(name.count("o"))

# Replace characters in string
print(name.replace("d","l"))

# Display string multiple times
print(name*3)
print((name+" ")*3)
