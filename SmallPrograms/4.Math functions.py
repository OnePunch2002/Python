import math


# Nearest round number
x = -3.14
print("nearest round: ",round(x)) 


# Round up number
x = 3.14
print("round up: ",math.ceil(x))


# Round down number
x = 3.14
print("round down: ",math.floor(x))


# Absolute value     (distance from zero on a number line)
x = 3.14
print("absolute value: ",abs(x))


# The power (or exponent) of a number
x = 6
print("power :",pow(x,2))


# Square root
x = 4761
print("sq root: ",math.sqrt(x))


# max value
x = 1
y = 2
z = 3
print("max: ",max(x,y,z))


# min value
x = 1
y = 2
z = 3
print("min: ",min(x,y,z))
